<div class="texto">
    <p>Emergia é uma medida de toda a energia utilizada para realizar um processo, expressa em uma unidade comum de energia solar, o joules de emergia solar (seJ).</p>
    <p>Diferente da energia comum, a emergia calcula todo o esforço ambiental e humano necessário para produzir algo. Ela soma desde recursos naturais (sol, água, vento, etc.) até trabalho e materiais industriais. Com isso, podemos entender melhor o verdadeiro valor e a sustentabilidade de recursos e sistemas.</p>
